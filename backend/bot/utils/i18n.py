def all_text_variants(key: str) -> Set[str]:
    variants = set()
    for language in SUPPORTED_LANGUAGES:
        value = TRANSLATIONS.get(language, {}).get(key)
        if value:
            variants.add(value)
    fallback = TRANSLATIONS['en'].get(key)
    if fallback:
        variants.add(fallback)
    return variants


def is_menu_text(text: str | None, key: str) -> bool:
    candidate = (text or '').strip()
    return bool(candidate) and candidate in all_text_variants(key)
