def normalize_language(language: str | None) -> str:
    code = (language or '').strip().lower()
    if code in SUPPORTED_LANGUAGES:
        return code
    return 'en'


def tr(language: str | None, key: str, **kwargs) -> str:
    lang = normalize_language(language)
    value = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS['en'].get(key, key)
    if kwargs:
        return value.format(**kwargs)
    return value


def language_name(language: str | None, ui_language: str | None = None) -> str:
    code = normalize_language(language)
    ui_lang = normalize_language(ui_language)
    return tr(ui_lang, f'lang_name_{code}')


